time_of_year <- c("autumn", "winter", "spring", "summer")

for (season in time_of_year) {
  
if (season == "autumn") {
  print("I love the fall most of all")
} else if (season == "summer") {
  print("summer is another favorite, mainly because of the sun and the beach")
} else if (season == "spring") {
  print("the spring is great once it's actually warmer in May")
} else {
  print("if it's winter, I only want snow if it makes for a White Christmas")
}
  
}